export function getProductReviews(productId) {
  return reviews.filter(r => r.productId === productId);
}

export function getAverageRating(productId) {
  const productReviews = getProductReviews(productId);
  if (productReviews.length === 0) return { average: 0, count: 0 };
  const sum = productReviews.reduce((acc, r) => acc + r.rating, 0);
  return { average: +(sum / productReviews.length).toFixed(1), count: productReviews.length };
}

export function getFitPercentage(productId) {
  const productReviews = getProductReviews(productId);
  if (productReviews.length === 0) return 0;
  const trueToSize = productReviews.filter(r => r.fit === "true-to-size").length;
  return Math.round((trueToSize / productReviews.length) * 100);
}
